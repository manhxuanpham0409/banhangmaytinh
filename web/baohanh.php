  <p><h3>3. Phương thức bảo hành:</h3></p>
 
    Tất cả các thiết bị được bảo hành miễn phí trong suốt thời hạn bảo hành. 
Hàng  mới mua trong vòng 05 ngày sẽ được đổi ngay hàng mới nếu việc kiểm tra hàng đó hư hỏng do lỗi của nhà sản xuất. 
Trong trường hợp không có hàng mới để đổi thì sẽ thỏa thuận đổi sang hàng mới khác có giá trị tương đương hoặc sẽ hoàn trả lại đúng số tiền mà quý khách đã mua. Chú ý : không áp dụng đối với các thiết bị như : máy in, mực in, các thiết bị có tính chất hao mòn, các thiết bị bị cắt rời, bẻ gãy, làm mất bao bì hoặc bị trầy xước, do bẩn. 
Trường hợp hàng đã mua quá thời hạn 05 ngày thì sẽ được nhận lại để bảo hành (sửa chữa). Nếu không thể sửa chữa được thì cửa hàng sẽ thay thế một sản phẩm khác tương đương và sản phẩm này không nhất thiết mới 100%. 
Thời gian giải quyết bảo hành từ 3-7 ngày kể từ ngày nhận (trừ ngày Chủ Nhật và các ngày lễ) và tùy từng trường hợp có thể giải quyết sớm hơn hoặc chậm hơn. 
Đối với các thiết bị không thể sửa chữa được mà thiết bị này hết hàng do không còn sản xuất nữa hoặc không còn lưu hành trên thị trường, quý khách phải đợi nhà phân phối đổi hàng khác có giá trị tương đương hoặc cao hơn và bù tiền theo thỏa thuận của giá cả hiện hành trên thị trường. Thời hạn bảo hành đối với sản phẩm được thay thế sẽ được tính tiếp tục chứ không bảo hành lại từ đầu. 
Đối với các thiết bị không sửa chữa được trong nước phải gởi sang nhả sản xuất ở nước ngoài thì thời hạn có thể kéo dài từ 4 đến 6 tuần. Trong trường hợp này cửa hàng sẽ thay thế một sản phẩm có tính năng tương đương để quý khách tạm sử dụng.  
