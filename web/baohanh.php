  <p><h3>2. Điều kiện không bảo hành:</h3></p>
 
    Máy hoặc thiết bị có dấu hiệu có dấu hiệu dung dịch lạ hoặc có nước vào. 
Máy hoặc thiết bị do va chạm hoặc đã bị rơi rớt, bể mẻ, móp méo, biến dạng, trầy xước, vết ẩm ướt, gỉ sét, phù tụ. 
Thiết bị có dấu hiệu cháy nổ, chuột bọ, côn trùng xâm nhập hoặc được sử dụng trong môi trường ẩm ướt. 
Hư hỏng do thiên tai hỏa hoạn, sử dụng nguồn điện không ổn định hoặc  do lắp đặt, vận chuyển không đúng quy cách, thiết bị bị đứt mạch, cháy phù, nám  .v.v. 
Không bảo hành các thiết bị phụ kiện như: bộ sạc, tai nghe, cable nối, vỏ máy, nút công tắc, dây điện, remote điều khiển, quạt giải nhiệt, sản phẩm tặng kèm theo chương trình khuyến mãi. 
Các thiết bị mainboard bị cong chân socket, CPU bị cong hoặc gãy chân. 
Không chịu trách nhiệm bảo hành dữ liệu có chứa trong thiết bị lưu trữ của khách hàng khi bảo hành thiết bị. 
Đối với máy in, chúng tôi không bảo hành bao lụa, đầu phun mực. 
Không chịu trách nhiệm bảo hành Bản quyền  cho các bản quyền MS WINDOWS, MS OFFICE, phần mềm, đĩa Driver .v.v. 
Các trường hợp bàn phím của máy bị bong, gãy chấu, bể, vỡ .v.v. 
 
