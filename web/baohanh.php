  <p> <h3>1. Điều kiện bảo hành:</h3></p>
 
    Tất cả các linh kiện, thiết bị phải có tem bảo hành của Star computer và tem phải còn trong thời hạn bảo hành. 
Tem bảo hành, mã vạch seri number phải còn nguyên vẹn, không có dấu hiệu cạo sửa, tẩy xóa hay bị rách mờ. 
Những hư hỏng  của thiết bị được xác định do lỗi kỹ thuật hoặc lỗi của nhà sản xuất. 
Các sản phẩm mainboard hoặc CPU  khi bảo hành cần phải có nắp đậy. 
Đối với các linh kiện, thiết bị được cấp phiếu bảo hành chính hãng như : máy bộ, Laptop, máy in, máy scanner, sản phẩm của intel .v.v.v :Khi mua các thiết bị, linh kiện này quý khách sẽ nhận được phiếu bảo hành, trên phiếu có ghi rõ các điều kiện bảo hành, địa chỉ bảo hành, quyền lợi của khách hàng…. Khách hàng phải xuất trình phiếu bảo hành khi bảo hành sản phẩm trực tiếp tại trung tâm bảo hành ghi trên phiếu hoặc tại cửa hàng Star computer. 
Màn hình LCD của máy bị điểm chấm có số điểm chết từ 05 điểm trở lên 
 
